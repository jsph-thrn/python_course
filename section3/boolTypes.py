# Boolean types

# Directly declaring a boolean value

myBoolean = False
print(myBoolean)

# Getting a boolean value from a logical operand

myBoolean = 1 > 2

# As 2 > 1, myBoolean value should be False

print(myBoolean)

# Using if condition

# If boolean variable is True

if myBoolean:
    print("Return is True")

# If boolean is False

else:
    print("Return is False")
