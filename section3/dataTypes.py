# Basic Data Types

x = 10
print(x)

# type() function returns the type of the entered argument
# Using print() with type function as argument, x type should be printed in console.

print(type(x))

# Declaring x variable as a string
# Aditionally, adding a hint of type (which doesn't define variable type, it is just
# for informative porpuses).

x : str = "Hello world"
print(x)
print(type(x))

# Declaring x variable as a float

x = 99.9
print(x)
print(type(x))

# Declaring x variable as a boolean; which can be either False or True

x = False
print(x)
print(type(x))
