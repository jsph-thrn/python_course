# Assignation

myVar = 10
print(myVar)

# Reassignation

myVar = myVar + 1
print(myVar)

# Delta with reassignation (short), the same as the last operation

myVar += 1
print(myVar)

# With substraction

myVar -= 2
print(myVar)

# With multiplication

myVar *= 3
print(myVar)

# With division

myVar /= 2
print(myVar)

