# Addition

opA = 3
opB = 2
add = opA + opB

# Simple print

# print('Sum result:', add)

# Interpolation (preferred print)

print(f'Sum result: {add} ')

# Substraction

dif = opA - opB
print(f'Sustraction result: {dif} ')

# Multiplication

pro = opA * opB
print(f'Multiplication product: {pro} ')

# Division

quo = opA / opB
print(f'Division quotient: {quo} ')

# Division (integer)

intQuo = opA // opB
print(f'Division quitient (integer): {intQuo}' )

# Module

mod = opA % opB
print(f'Division residue (module): {mod}' )

# Exponentiation

exp = opA ** opB
print(f'Exponentiation power: {exp}' )
