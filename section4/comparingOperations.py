# Comparing operations

a = 4
b = 2

# Equal

res = a == b
print(f'Result == {res}')

# Different

res = a != b
print(f'Result != {res}')

# Bigger than

res = a > b
print(f'Result > {res}')

# Greater than or equal 

res = a >= b
print(f'Res >= {res}')

# Less than

res = a < b
print(f'Res < {res}')

# Less than or equal

res = a <= b
print(f'Res <= {res}')
