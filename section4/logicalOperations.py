# Logical operations (boolean)

a = True
b = False

# AND (binary)
# If and only if both operands are True, result will be True. Otherwise, result will be false.

res = a and b
print(res)

# OR (binary)
# If any or both operands are True, result will be True. Otherwise, False.
res = a or b
print(res)

# NOT (unary)
# Inverts the value of the operand

res = not a
print(res)
